//High level Disk Functions
//Uses spi read/write functions defined in sdcard.h
//Disk initialization relies on SD_GoIdle() to get it
//into the idle state.  The disk init function below
//called from fmount does not reliably reset the card
//and get it into idle state.  SD_GoIdle is called from
//SD_init().  Putting the card into idle before trying
//mount the drive seems to work pretty good.
//
